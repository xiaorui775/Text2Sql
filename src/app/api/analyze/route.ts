type AnalyzeStage = 'optimization' | 'analysis' | 'design' | 'sql_generation' | 'doc_generation'

interface CallLLMOptions {
  timeoutMs: number
  maxRetries: number
  stageName: AnalyzeStage
  useStream?: boolean
}

const ANALYZE_TOTAL_BUDGET_MS = 300000
const HEARTBEAT_INTERVAL_MS = 12000
const STAGE_TIMEOUT_MS: Record<AnalyzeStage, number> = {
  optimization: 50000,
  analysis: 40000,
  design: 90000,
  sql_generation: 60000,
  doc_generation: 50000
}

function sleep(ms: number) {
  return new Promise(resolve => setTimeout(resolve, ms))
}

function getRemainingBudgetMs(startTime: number): number {
  return ANALYZE_TOTAL_BUDGET_MS - (Date.now() - startTime)
}

function getCallOptions(stageName: AnalyzeStage, startTime: number): CallLLMOptions {
  const remaining = getRemainingBudgetMs(startTime)
  if (remaining <= 15000) {
    throw new Error('分析总耗时已接近上限，请简化需求后重试')
  }
  const timeoutMs = Math.max(15000, Math.min(STAGE_TIMEOUT_MS[stageName], remaining - 5000))
  return {
    timeoutMs,
    maxRetries: 1,
    stageName
  }
}

function getDocCallOptions(startTime: number): CallLLMOptions | null {
  const remaining = getRemainingBudgetMs(startTime)
  if (remaining <= 12000) return null
  const timeoutMs = Math.max(8000, Math.min(25000, remaining - 3000))
  return {
    timeoutMs,
    maxRetries: 0,
    stageName: 'doc_generation'
  }
}

function shouldFallbackToNonStream(error: unknown, stageName: AnalyzeStage): boolean {
  if (stageName !== 'design' && stageName !== 'sql_generation') return false
  if (!(error instanceof Error)) return false
  const message = error.message.toLowerCase()
  return message.includes('failed to parse llm response as json')
    || message.includes('llm api returned empty content')
    || message.includes('llm api returned invalid json')
    || message.includes('unexpected end')
    || (stageName === 'sql_generation' && message.includes('sql结果校验失败'))
}

