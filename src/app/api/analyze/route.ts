function extractContentText(content: unknown): string {
  if (typeof content === 'string') return content
  if (Array.isArray(content)) {
    return content
      .map(item => {
        if (typeof item === 'string') return item
        if (item && typeof item === 'object' && 'text' in item) {
          const text = (item as { text?: unknown }).text
          return typeof text === 'string' ? text : ''
        }
        return ''
      })
      .join('')
  }
  return ''
}

function extractBalancedJsonObject(input: string): string | null {
  const text = input.trim()
  const startIndex = text.indexOf('{')
  if (startIndex === -1) return null

  let depth = 0
  let inString = false
  let escaped = false
  const stack: string[] = []

  for (let i = startIndex; i < text.length; i++) {
    const char = text[i]
    if (inString) {
      if (escaped) {
        escaped = false
      } else if (char === '\\') {
        escaped = true
      } else if (char === '"') {
        inString = false
        stack.pop()
      }
      continue
    }

    if (char === '"') {
      inString = true
      stack.push('"')
      continue
    }

    if (char === '{') {
      depth++
      stack.push('}')
    } else if (char === '[') {
      stack.push(']')
    } else if (char === '}') {
      depth--
      if (stack[stack.length - 1] === '}') stack.pop()
      if (depth === 0) {
        return text.slice(startIndex, i + 1)
      }
    } else if (char === ']') {
      if (stack[stack.length - 1] === ']') stack.pop()
    }
  }

  // 如果 JSON 不完整，尝试修复
  if (depth > 0) {
    let fixed = text.slice(startIndex)
    // 移除末尾的逗号
    fixed = fixed.replace(/,\s*$/, '')
    // 补全缺失的括号
    while (stack.length > 0) {
      const closer = stack.pop()
      if (closer) fixed += closer
    }
    return fixed
  }

  return null
}

// 新增：更激进的 JSON 修复函数
function aggressiveJsonFix(input: string): string | null {
  let text = input.trim()
  
  // 移除 markdown 代码块
  text = text.replace(/^```(?:json)?\s*/i, '').replace(/\s*```$/i, '')
  
  // 提取 JSON 对象
  const extracted = extractBalancedJsonObject(text)
  if (!extracted) return null
  
  let fixed = extracted
  
  // 修复常见的 JSON 语法错误
  // 1. 移除对象/数组末尾的逗号
  fixed = fixed.replace(/,(\s*[}\]])/g, '$1')
  
  // 2. 修复未闭合的字符串（在逗号或括号前）
  fixed = fixed.replace(/("[^"]*?)(\n|$)/g, (match, p1) => {
    if (!p1.endsWith('"')) return p1 + '"'
    return match
  })
  
  // 3. 移除注释
  fixed = fixed.replace(/\/\*[\s\S]*?\*\//g, '')
  fixed = fixed.replace(/\/\/.*/g, '')
  
  // 4. 修复中文标点
  fixed = fixed.replace(/，/g, ',')
  fixed = fixed.replace(/：/g, ':')
  
  return fixed
}

function parseStructuredJsonContent(raw: string): any {
  let cleanedContent = raw.trim()
  
  // 移除 markdown 代码块
  const jsonBlockMatch = cleanedContent.match(/```json\s*([\s\S]*?)\s*```/)
  if (jsonBlockMatch) {
    cleanedContent = jsonBlockMatch[1]
  } else {
    const codeBlockMatch = cleanedContent.match(/```\s*([\s\S]*?)\s*```/)
    if (codeBlockMatch) {
      cleanedContent = codeBlockMatch[1]
    }
  }

  // 尝试多种修复策略
  const candidates = [
    cleanedContent,
    cleanedContent.replace(/,\s*([}\]])/g, '$1'), // 移除末尾逗号
    aggressiveJsonFix(cleanedContent) // 激进修复
  ].filter(Boolean) as string[]

  // 尝试解析每个候选
  for (const candidate of candidates) {
    try {
      const parsed = JSON.parse(candidate)
      // 验证解析结果是否为对象
      if (parsed && typeof parsed === 'object') {
        return parsed
      }
    } catch (e) {
      // 继续尝试下一个候选
      continue
    }
  }

  throw new Error('Failed to parse LLM response as JSON')
}

function isSqlContentValid(sql: string): boolean {
  const normalized = sql.toUpperCase()
  if (normalized.length < 20) return false
  return /(CREATE\s+(TABLE|INDEX|SEQUENCE|VIEW)|ALTER\s+TABLE|DROP\s+TABLE)/.test(normalized)
}

async function extractContentFromStandardResponse(response: Response): Promise<string> {
  const text = await response.text()
  let data
  try {
    data = JSON.parse(text)
  } catch (e) {
    console.error('LLM API returned non-JSON response:', text)
    throw new Error(`LLM API returned invalid JSON: ${text.slice(0, 100)}...`)
  }
  const content = extractContentText(data?.choices?.[0]?.message?.content)
  if (!content) {
    throw new Error('LLM API returned empty content')
  }
  return content
}

async function extractContentFromStreamResponse(response: Response): Promise<string> {
  if (!response.body) {
    throw new Error('LLM API 返回了空响应流')
  }

  const reader = response.body.getReader()
  const decoder = new TextDecoder()
  let buffer = ''
  let content = ''

  while (true) {
    const { done, value } = await reader.read()
    if (done) {
      buffer += decoder.decode()
    } else {
      buffer += decoder.decode(value, { stream: true })
    }

    const events = buffer.split('\n\n')
    buffer = events.pop() || ''

    for (const eventBlock of events) {
      const dataLines = eventBlock
        .split('\n')
        .map(line => line.trim())
        .filter(line => line.startsWith('data:'))
        .map(line => line.slice(5).trimStart())

      if (dataLines.length === 0) continue

      for (const payload of dataLines) {
        if (!payload) continue
        if (payload === '[DONE]') {
          return content.trim()
        }

        let chunk: any
        try {
          chunk = JSON.parse(payload)
        } catch (e) {
          console.warn('Failed to parse stream payload:', payload)
          continue
        }

        if (chunk?.error?.message) {
          throw new Error(chunk.error.message)
        }

        const deltaText = extractContentText(chunk?.choices?.[0]?.delta?.content)
        const messageText = extractContentText(chunk?.choices?.[0]?.message?.content)

        if (deltaText) {
          content += deltaText
        } else if (messageText) {
          const normalizedMessageText = messageText.trim()
          if (normalizedMessageText.startsWith(content.trim())) {
            content = normalizedMessageText
          } else {
            content += messageText
          }
        }
      }
    }

    if (done) break
  }

  if (!content) {
    throw new Error('LLM API returned empty content')
  }
  return content
}

async function callLLM(
  config: LLMConfig, 
  systemPrompt: string, 
  userMessage: string, 
  expectJson: boolean = true, 
  textKey?: string,
  options: CallLLMOptions = { timeoutMs: 120000, maxRetries: 1, stageName: 'analysis' },
  retryCount: number = 0
): Promise<any> {
  const baseUrl = config.baseUrl || 'https://api.openai.com/v1'
  const url = `${baseUrl}/chat/completions`
  const useStream = options.useStream ?? true

  const controller = new AbortController()
  const timeoutId = setTimeout(() => controller.abort(), options.timeoutMs)

  try {
    const response = await fetch(url, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${config.apiKey}`
      },
      body: JSON.stringify({
        model: config.model,
        messages: [
          { role: 'system', content: systemPrompt },
          { role: 'user', content: userMessage }
        ],
        stream: useStream,
        temperature: config.temperature,
        max_tokens: config.maxTokens
      }),
      signal: controller.signal
    })

    clearTimeout(timeoutId)

    if (!response.ok) {
      const errorText = await response.text()
      if ((response.status === 429 || response.status >= 500) && retryCount < options.maxRetries) {
        const backoff = 1200 * (retryCount + 1) + Math.floor(Math.random() * 400)
        await sleep(backoff)
        return callLLM(config, systemPrompt, userMessage, expectJson, textKey, options, retryCount + 1)
      }
      throw new Error(`LLM API 调用失败: ${response.status} - ${errorText}`)
    }

    const content = useStream
      ? await extractContentFromStreamResponse(response)
      : await extractContentFromStandardResponse(response)

    if (!expectJson && textKey) {
        let cleanedContent = content.trim()
        cleanedContent = cleanedContent.replace(/^```[\w]*\n?/i, '')
                                       .replace(/\n?```$/i, '')
                                       .trim()
        if (textKey === 'sqlStatements' && !isSqlContentValid(cleanedContent)) {
          throw new Error('SQL结果校验失败：未检测到有效 DDL 语句')
        }
        return { [textKey]: cleanedContent }
    }

    try {
      return parseStructuredJsonContent(content)
    } catch (e) {
      const normalizedContent = content.trim()
      const parseSample = normalizedContent.slice(0, 300).replace(/\s+/g, ' ')
      console.error(`[${options.stageName}] JSON parse failed sample:`, parseSample)
      
      // 新增：如果是第一次失败，尝试让 LLM 自己修复 JSON
      if (retryCount === 0 && expectJson) {
        console.log(`[${options.stageName}] Attempting JSON self-repair...`)
        const repairSource = normalizedContent.length > 16000 ? normalizedContent.slice(0, 16000) : normalizedContent
        const repairPrompt = `以下是一个格式错误的 JSON，请修复它并返回正确的 JSON 格式。只返回修复后的 JSON，不要任何解释。

错误的 JSON：
${repairSource}

请返回修复后的完整 JSON 对象。`
        
        try {
          const repaired = await callLLM(
            config,
            '你是一个 JSON 格式修复专家。',
            repairPrompt,
            true,
            undefined,
            { ...options, maxRetries: 0 },
            1 // 标记为重试，避免无限循环
          )
          return repaired
        } catch (repairError) {
          console.error(`[${options.stageName}] JSON self-repair failed:`, repairError)
          // 继续执行原有的容错逻辑
        }
      }
      
      // 容错处理：如果模型没有返回 JSON 而是直接返回了 SQL 代码
      if (!normalizedContent.includes('{') && (normalizedContent.toUpperCase().includes('CREATE TABLE') || normalizedContent.toUpperCase().includes('CREATE DATABASE'))) {
        return {
          sqlStatements: content.replace(/```sql/gi, '').replace(/```/g, '').replace(/^sql\n/i, '').trim()
        }
      }
      // 容错处理：如果是文档生成阶段返回了纯 Markdown
      if (!normalizedContent.includes('{') && normalizedContent.includes('#')) {
         return {
            designDocument: content.replace(/```markdown/gi, '').replace(/```/g, '').trim()
         }
      }
      throw new Error(`Failed to parse LLM response as JSON (stage: ${options.stageName})`)
    }

  } catch (error) {
    clearTimeout(timeoutId)
    if (error instanceof Error && error.name === 'AbortError') {
      if (retryCount < options.maxRetries) {
        const backoff = 1200 * (retryCount + 1) + Math.floor(Math.random() * 400)
        await sleep(backoff)
        return callLLM(config, systemPrompt, userMessage, expectJson, textKey, options, retryCount + 1)
      }
      throw new Error(`阶段 ${options.stageName} 调用超时（${Math.floor(options.timeoutMs / 1000)}秒），请重试或简化需求`)
    }
    if (error instanceof Error && retryCount < options.maxRetries) {
      const causeCode = (error as Error & { cause?: { code?: string } }).cause?.code
      const lowerMessage = error.message.toLowerCase()
      const isRetryableNetworkError = causeCode === 'UND_ERR_CONNECT_TIMEOUT'
        || lowerMessage.includes('fetch failed')
        || lowerMessage.includes('network')
        || lowerMessage.includes('connect timeout')
      const isRetryableParseError = lowerMessage.includes('failed to parse llm response as json')
      const isRetryableSqlValidationError = options.stageName === 'sql_generation' && lowerMessage.includes('sql结果校验失败')
      if (isRetryableNetworkError) {
        const backoff = 1200 * (retryCount + 1) + Math.floor(Math.random() * 400)
        await sleep(backoff)
        return callLLM(config, systemPrompt, userMessage, expectJson, textKey, options, retryCount + 1)
      }
      if (isRetryableParseError) {
        const backoff = 800 * (retryCount + 1) + Math.floor(Math.random() * 300)
        await sleep(backoff)
        return callLLM(config, systemPrompt, userMessage, expectJson, textKey, options, retryCount + 1)
      }
      if (isRetryableSqlValidationError) {
        const backoff = 800 * (retryCount + 1) + Math.floor(Math.random() * 300)
        await sleep(backoff)
        return callLLM(config, systemPrompt, userMessage, expectJson, textKey, options, retryCount + 1)
      }
    }
    throw error
  }
}

export async function POST(request: NextRequest) {
  let writer: WritableStreamDefaultWriter<Uint8Array> | null = null;

  try {
    const toBoolean = (value: unknown, fallback: boolean) => {
      if (typeof value === 'boolean') return value
      if (typeof value === 'string') {
        const normalized = value.trim().toLowerCase()
        if (normalized === 'true') return true
        if (normalized === 'false') return false
      }
      return fallback
    }

    const body = await request.json()
    const { requirement, options } = body
    const enableOptimization = toBoolean(options?.enableOptimization, true)
    const enableDocGeneration = toBoolean(options?.enableDocGeneration, true)

    if (!requirement || typeof requirement !== 'string') {
      return NextResponse.json(
        { error: '请提供有效的需求描述' },
        { status: 400 }
      )
    }

    // 获取 LLM 配置
    const llmConfig = await db.lLMConfig.findFirst({
      where: { isActive: true }
    })

    if (!llmConfig || !llmConfig.apiKey) {
      return NextResponse.json(
        { error: '请先配置 LLM 服务', needConfig: true },
        { status: 400 }
      )
    }

    // Check cache
    const shouldUseCache = enableOptimization && enableDocGeneration
    const cachedHistory = shouldUseCache
      ? await db.history.findFirst({
          where: {
            question: requirement,
            status: 'success',
            databaseType: llmConfig.databaseType || 'mysql',
            createdAt: {
              gte: new Date(Date.now() - 24 * 60 * 60 * 1000)
            }
          },
          orderBy: { createdAt: 'desc' }
        })
      : null

    if (cachedHistory) {
        // If cached, return immediately (not streaming for simplicity, or simulate stream)
        // To be consistent with frontend, we should probably return JSON directly or stream it.
        // Let's stream the cached result to keep frontend logic unified.
        const cachedResult = JSON.parse(cachedHistory.result)
        
        const stream = new TransformStream()
        const writer = stream.writable.getWriter()
        const encoder = new TextEncoder()

        // Async IIFE to write to stream
        ;(async () => {
            await writer.write(encoder.encode(`event: final_result\ndata: ${JSON.stringify(cachedResult)}\n\n`))
            await writer.close()
        })()

        return new NextResponse(stream.readable, {
            headers: {
                'Content-Type': 'text/event-stream',
                'Cache-Control': 'no-cache',
                'Connection': 'keep-alive',
            }
        })
    }

    const stream = new TransformStream()
    writer = stream.writable.getWriter()
    const encoder = new TextEncoder()

    const sendEvent = async (event: string, data: any) => {
        await writer!.write(encoder.encode(`event: ${event}\ndata: ${JSON.stringify(data)}\n\n`))
    }

    const withStageHeartbeat = async <T>(stage: AnalyzeStage, fn: () => Promise<T>): Promise<T> => {
        const timer = setInterval(() => {
            void sendEvent('heartbeat', { stage, timestamp: Date.now() })
        }, HEARTBEAT_INTERVAL_MS)
        try {
            return await fn()
        } finally {
            clearInterval(timer)
        }
    }

    // Start processing in background
    ;(async () => {
        let currentStage: AnalyzeStage = 'optimization'
        try {
            const config = {
                provider: llmConfig.provider,
                apiKey: llmConfig.apiKey,
                baseUrl: llmConfig.baseUrl,
                model: llmConfig.model,
                temperature: llmConfig.temperature,
                maxTokens: llmConfig.maxTokens,
                databaseType: llmConfig.databaseType || 'mysql'
            }
            const startedAt = Date.now()

            // Stage 1: Requirement Optimization
            currentStage = 'optimization'
            let optimizedRequirement = requirement;
            let optimizationResult: any = { optimizedRequirement: requirement };
            
            if (enableOptimization) {
                await sendEvent('stage_start', { stage: 'optimization', message: '正在提取关键信息并精炼需求...' })
                optimizationResult = await withStageHeartbeat('optimization', () =>
                  callLLM(config, getRequirementOptimizationPrompt(), requirement, false, 'optimizedRequirement', getCallOptions('optimization', startedAt))
                )
                if (!optimizationResult || typeof optimizationResult.optimizedRequirement !== 'string') {
                    throw new Error('需求优化阶段返回数据格式错误')
                }
                await sendEvent('stage_done', { stage: 'optimization', data: optimizationResult })
                optimizedRequirement = optimizationResult.optimizedRequirement;
            } else {
                await sendEvent('stage_start', { stage: 'optimization', message: '需求优化已关闭，跳过该阶段...' })
                await sendEvent('stage_done', { stage: 'optimization', data: optimizationResult })
            }

            // Stage 2: Requirement Analysis
            currentStage = 'analysis'
            await sendEvent('stage_start', { stage: 'analysis', message: '正在提取核心关键点...' })
            const analysisResult = await withStageHeartbeat('analysis', () =>
              callLLM(config, getAnalysisPrompt(), optimizedRequirement, true, undefined, getCallOptions('analysis', startedAt))
            )
            if (!analysisResult || !Array.isArray(analysisResult.keyPoints)) {
                throw new Error('需求分析阶段返回数据格式错误')
            }
            await sendEvent('stage_done', { stage: 'analysis', data: analysisResult })

            // Stage 3: Schema Design
            currentStage = 'design'
            await sendEvent('stage_start', { stage: 'design', message: '正在设计表结构...' })
            const designOptions = getCallOptions('design', startedAt)
            let schemaResult: any
            try {
                schemaResult = await withStageHeartbeat('design', () =>
                  callLLM(config, getSchemaDesignPrompt(config.databaseType, analysisResult.keyPoints), JSON.stringify(analysisResult), true, undefined, { ...designOptions, useStream: true })
                )
            } catch (error) {
                if (!shouldFallbackToNonStream(error, 'design')) {
                    throw error
                }
                schemaResult = await withStageHeartbeat('design', () =>
                  callLLM(config, getSchemaDesignPrompt(config.databaseType, analysisResult.keyPoints), JSON.stringify(analysisResult), true, undefined, { ...designOptions, useStream: false, maxRetries: 0 })
                )
            }
            if (!schemaResult || typeof schemaResult !== 'object') {
                throw new Error('表结构设计阶段返回数据格式错误')
            }
            if (!Array.isArray(schemaResult.tables)) {
                schemaResult.tables = []
            }
            if (!Array.isArray(schemaResult.relations)) {
                schemaResult.relations = []
            }
            if (schemaResult.tables.length === 0) {
                throw new Error('表结构设计阶段返回数据格式错误: 缺少表数据')
            }
            await sendEvent('stage_done', { stage: 'design', data: schemaResult })

            // Stage 4: SQL Generation
            currentStage = 'sql_generation'
            await sendEvent('stage_start', { stage: 'sql_generation', message: '正在生成 SQL 语句...' })
            const sqlOptions = getCallOptions('sql_generation', startedAt)
            let sqlGenerationResult: any
            try {
                sqlGenerationResult = await withStageHeartbeat('sql_generation', () =>
                  callLLM(config, getSqlGenerationPrompt(config.databaseType, schemaResult.tables, schemaResult.relations), JSON.stringify(schemaResult), false, 'sqlStatements', { ...sqlOptions, useStream: true })
                )
            } catch (error) {
                if (!shouldFallbackToNonStream(error, 'sql_generation')) {
                    throw error
                }
                console.warn('[sql_generation] stream failed, fallback to non-stream:', error instanceof Error ? error.message : String(error))
                sqlGenerationResult = await withStageHeartbeat('sql_generation', () =>
                  callLLM(config, getSqlGenerationPrompt(config.databaseType, schemaResult.tables, schemaResult.relations), JSON.stringify(schemaResult), false, 'sqlStatements', { ...sqlOptions, useStream: false, maxRetries: 0 })
                )
            }
            if (!sqlGenerationResult || typeof sqlGenerationResult.sqlStatements !== 'string') {
                 throw new Error('SQL生成阶段返回数据格式错误')
            }
            await sendEvent('stage_done', { stage: 'sql_generation', data: sqlGenerationResult })

            // Stage 5: Documentation Generation
            currentStage = 'doc_generation'
            let docGenerationResult: { designDocument: string } = { designDocument: '' }
            
            if (enableDocGeneration) {
                await sendEvent('stage_start', { stage: 'doc_generation', message: '正在生成设计文档...' })
                try {
                    const docOptions = getDocCallOptions(startedAt)
                    if (!docOptions) {
                        const message = '剩余预算不足，已跳过大模型文档生成并返回兜底文档'
                        docGenerationResult = {
                            designDocument: `# 设计文档未完整生成\n\n原因：${message}\n\n以下为模板化兜底文档：\n\n${buildFallbackDesignDocument(config.databaseType, schemaResult.tables, schemaResult.relations)}`
                        }
                        await sendEvent('stage_done', { stage: 'doc_generation', data: docGenerationResult, partial: true, error: message })
                    } else {
                    const compactDocInput = buildCompactDocInput(schemaResult.tables, schemaResult.relations)
                    docGenerationResult = await withStageHeartbeat('doc_generation', () =>
                      callLLM(config, getDocumentGenerationPrompt(schemaResult.tables, schemaResult.relations), compactDocInput, false, 'designDocument', docOptions)
                    )
                    if (!docGenerationResult || typeof docGenerationResult.designDocument !== 'string') {
                        throw new Error('文档生成阶段返回数据格式错误')
                    }
                    const docIsStructured = isDocumentStructured(docGenerationResult.designDocument)
                    const docHasAllTables = hasAllTablesCovered(docGenerationResult.designDocument, schemaResult.tables)
                    if (!docIsStructured || !docHasAllTables) {
                        const repairOptions = getDocCallOptions(startedAt)
                        if (repairOptions && getRemainingBudgetMs(startedAt) > 18000) {
                            const repaired = await withStageHeartbeat('doc_generation', () =>
                              callLLM(config, getDocumentRepairPrompt(docGenerationResult.designDocument), '', false, 'designDocument', repairOptions)
                            )
                            if (repaired && typeof repaired.designDocument === 'string' && isDocumentStructured(repaired.designDocument) && hasAllTablesCovered(repaired.designDocument, schemaResult.tables)) {
                                docGenerationResult = repaired
                            } else {
                                docGenerationResult = {
                                    designDocument: buildFallbackDesignDocument(config.databaseType, schemaResult.tables, schemaResult.relations)
                                }
                            }
                        } else {
                            docGenerationResult = {
                                designDocument: buildFallbackDesignDocument(config.databaseType, schemaResult.tables, schemaResult.relations)
                            }
                        }
                    }
                    await sendEvent('stage_done', { stage: 'doc_generation', data: docGenerationResult })
                    }
                } catch (error) {
                    const message = error instanceof Error ? error.message : '文档生成超时'
                    docGenerationResult = {
                        designDocument: `# 设计文档未完整生成\n\n原因：${message}\n\n以下为模板化兜底文档：\n\n${buildFallbackDesignDocument(config.databaseType, schemaResult.tables, schemaResult.relations)}`
                    }
                    await sendEvent('stage_done', { stage: 'doc_generation', data: docGenerationResult, partial: true, error: message })
                }
            } else {
                await sendEvent('stage_start', { stage: 'doc_generation', message: '跳过生成设计文档...' })
                await sendEvent('stage_done', { stage: 'doc_generation', data: docGenerationResult })
            }

            // Combine results
            const finalResult = {
                optimizedRequirement: optimizationResult.optimizedRequirement,
                keyPoints: analysisResult.keyPoints,
                tables: schemaResult.tables,
                relations: schemaResult.relations,
                sqlStatements: sqlGenerationResult.sqlStatements,
                designDocument: docGenerationResult.designDocument,
                databaseType: config.databaseType
            }

            // Save history
            await db.history.create({
                data: {
                    question: requirement,
                    result: JSON.stringify(finalResult),
                    databaseType: config.databaseType,
                    provider: config.provider,
                    model: config.model,
                    status: 'success'
                }
            })

            await sendEvent('final_result', finalResult)
        } catch (error) {
            console.error('Streaming error:', error)
            const errorMessage = error instanceof Error ? error.message : 'Unknown error'
            const errorReason = errorMessage.toLowerCase().includes('failed to parse llm response as json')
              ? 'json_parse_failed'
              : errorMessage.includes('超时')
                ? 'timeout'
                : errorMessage.includes('校验失败')
                  ? 'validation_failed'
                  : 'unknown'
            await sendEvent('error', { message: errorMessage, stage: currentStage, reason: errorReason })
        } finally {
            await writer!.close()
        }
    })()

    return new NextResponse(stream.readable, {
        headers: {
            'Content-Type': 'text/event-stream',
            'Cache-Control': 'no-cache',
            'Connection': 'keep-alive',
        }
    })

  } catch (error) {
    console.error('Request error:', error)
    return NextResponse.json(
      { error: error instanceof Error ? error.message : 'Internal Server Error' },
      { status: 500 }
    )
  }
}
